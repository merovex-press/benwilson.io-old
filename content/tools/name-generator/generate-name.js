var haiku = function () {
  var adj = adjs[Math.floor(Math.random() * adjs.length)]; // http://stackoverflow.com/a/17516862/103058
  var noun = nouns[Math.floor(Math.random() * nouns.length)];
  var MIN = 1000;
  var MAX = 9999;
  var num = Math.floor(Math.random() * ((MAX + 1) - MIN)) + MIN;

  return adj + '-' + noun + '-' + num;
};

var updateTargetWithHaiku = function () {
  $('#result-goes-here').fadeOut(0);
  $('#result-goes-here').text(haiku());
  $('#result-goes-here').fadeIn(300);
};